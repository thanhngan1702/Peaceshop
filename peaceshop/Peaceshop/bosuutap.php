<script type="text/javascript">
$(function() {

    var $grid = $('#tp-grid'),
        $name = $('#name'),
        $close = $('#close'),
        $loadergl = $(
            '<div class="loadergl"><i></i><i></i><i></i><i></i><i></i><i></i><span>Loading...</span></div>')
        .insertBefore($grid),
        stapel = $grid.stapel({
            delay: 50,
            onLoad: function() {
                $loadergl.remove();
            },
            onBeforeOpen: function(pileName) {
                $name.html(pileName);
            },
            onAfterOpen: function(pileName) {
                $close.show();
            }
        });

    $close.on('click', function() {
        $close.hide();
        $name.empty();
        stapel.closePile();
    });

});
</script>

